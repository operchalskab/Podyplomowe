public class NazwyLiczb {
    public static void main(String[] args) {
        String a = args[0];

        switch (a) {
            case "jeden":
                System.out.println(1);
                break;
            case "dwa":
                System.out.println(2);
                break;
            case "trzy":
                System.out.println(3);
                break;
            case "cztery":
                System.out.println(4);
                break;
            case "pięć":
                System.out.println(5);
                break;
            case "sześć":
                System.out.println(6);
                break;
        }
    }
}